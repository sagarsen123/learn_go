package main

import "fmt"

func main() {
	//Define values

	// int
	fmt.Println(1 + 1)

	// string
	fmt.Println("Hello" + " " + "World")

	// bool
	fmt.Println(true)
	fmt.Println(!true)

	// float
	fmt.Println(1.5 + 2 + 2)

	// complex
	fmt.Println(complex(1, 2) + complex(3, 4))

	// division
	fmt.Println(5 / 2)

	// multiplication
	fmt.Println(5 * 2)

}
